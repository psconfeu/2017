<Window
   xmlns="http://schemas.microsoft.com/winfx/2006/xaml/presentation"
   xmlns:x="http://schemas.microsoft.com/winfx/2006/xaml"
   MinHeight="460"
   Width="460"
   SizeToContent="Height"
   Title="PowerShell WPF Window"
   Topmost="True">
    <Grid>
        <Grid.RowDefinitions>
            <RowDefinition Height="Auto"/>
        </Grid.RowDefinitions>
        <Image Name="Bild" HorizontalAlignment="Left" Height="350" Margin="48,43,0,0" Grid.RowSpan="1" VerticalAlignment="Top" Width="350"  Stretch="Fill"/>
    </Grid>
</Window>
'@

